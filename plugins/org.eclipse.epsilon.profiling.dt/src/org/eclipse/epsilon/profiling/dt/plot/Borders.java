public class Borders {
	public int left;
	public int right;
	public int top;
	public int bottom;
	
	public Borders(int left, int right, int top, int bottom) {
		super();
		this.left = left;
		this.right = right;
		this.top = top;
		this.bottom = bottom;
	}
	
}

